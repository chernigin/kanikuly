			<h3>Местоположение</h3>
			<p>
			Спортивно-оздоровительный комплекс "Витязь" расположен в живописном курортном местечке под названием Дивноморское, где также как и само название прекрасны и лазурное ласковое море открытой бухты и охраняемый благоустроенный чистый пляж и белоснежная набережная, и разнообразная архитектура современных строений, и окружающая природа, и утопающая в соснах и красивых ухоженных клумбах территория оздоровительного комплекса. Если посмотреть на "Витязь" сверху, то за пышной зеленью сосен едва различишь одноэтажные коттеджи с видом на море или тенистую аллею.
			</p>
