			<h3>Проживание</h3>
			<p>
			База отдыха «Витязь» представляет собой комплекс из комфортабельных коттеджей, различных по архитектуре, но одинаковых по уюту и комфорту. Современные благоустроенные, безопасные для проживания корпуса с 2-х, 3-х и 4-х местными номерами со всеми удобствами (туалет, ванна, горячая вода, холодильник, мебель). Свободная столовая, позволяющая кормление в одну смену. Питание 3-разовое сбалансированное, многообразное, обильное.
			</p>
		</div>
