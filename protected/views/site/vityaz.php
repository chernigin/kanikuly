	<div class="tab-pane" id="tab5">
		<div class='a'>
			<h3>Пляж</h3>
			<p>
			На расстоянии менее чем 50 м от домиков протянулся собственный галечный пляж, пройдя по которому метров 300, вы попадаете на общий городской пляж, оборудованный различными комплексами развлечений. Прямо на пляже размещаются точки проката гидроциклов и сопутствующих им аттракционов: таблетка, банан, водные лыжи. Гидроциклы в Дивноморском предлагают в прокат как с инструктором, так и без него. Для любителей острых ощущений владельцы специализированных катеров предлагают полет над бухтой на парашюте, буксируемый катером. Для безопасности полета, подъем осуществляется прямо с катера, в середине бухты.
			</p>
