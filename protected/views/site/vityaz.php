			<h3>О курорте</h3>
			<p>
			Курортный поселок Дивноморское входит в состав Геленджикской группы курортов Краснодарского края, находится в 12 км по автотрассе и в 9 км вдоль берега моря от Геленджика. Климат мягкий. Чистейшее море, шикарные пляжи, сосновый лес, хорошая инфраструктура делает отдых здесь незабываемым! Пляж в Дивноморском мелкогалечный и чистый, морское дно песчаное. Морская вода чистая и прозрачная. На центральном пляже предлагаются всевозможные аттракционы, скутеры, бананы, парашюты, морские прогулки, прокат аквалангов и гидроциклов и прочие водные развлечения.. В центре поселка специально для детского отдыха сооружен небольшой аквапарк, где детишки могут с удовольствием покататься на горках и поплескаться в бассейне. Детей постарше несомненно заинтересует автодром с картингом. Достопримечательности Дивноморского довольно разнообразны. Многие экскурсии из-за сравнительной близости Дивноморского к Геленджику организуются в Геленджике.
