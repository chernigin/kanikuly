			<h3>Досуг</h3>
			<p>
			Жизнь в спортивно-оздоровительном комплексе интересна и разнообразна: спортивные мероприятия, игры, соревнования, увлекательные путешествия и походы по окрестностям курорта, купания в море, различные тематические вечерние развлекательные мероприятия, конкурсные программы и веселые дискотеки. Для любителей активного досуга есть спортивный инвентарь, различные спортивные площадки для футбола, баскетбола, пионербола, волейбола, настольного тенниса, бадминтона. На базе отдыха имеется место для пикника с самостоятельным приготовлением шашлыка. Есть возможность посетить всевозможные экскурсии.
