			<h3 style="text-align:center;">История</h3>
			<p>В 1938 году Ростовский государственный университет заключил договор с дирекцией винкомбината Абрау-Дюрсо «на передачу ущелья «Лиманчик» университету для научных и учебных целей на 20 лет».</p>
			<p>Приказ о создании базы отдыха «Лиманчик» был подписан в 1958 году. Хотя сам лагерь «подпольно» функционировал с 1956 года.</p>
			<p>«Лиманчик» включает в себя не только базу отдыха, но и довольно большое (свыше 1000 человек) «дикарское» палаточное поселение, появляющееся вокруг базы на окружающих её горах. Лиманчик пользуется популярностью не только среди сотрудников, студентов и выпускников ЮФУ, но и среди людей со всей страны, а также других государств.</p>
