				<h4 style="text-align:center;" id="myModalLabel">Концепция тематической смены «Творческая деятельность как метод формирования социально-личностных компетенций конкурентоспособного специалиста»</h4>
			  </div>
			  <div class="modal-body">
				<p>Тематическая смена «Творческая деятельность как метод формирования социально-

личностных компетенций конкурентоспособного специалиста» (Арткоманда) нацелена 

на создание творческой, проектной, дискуссионной, познавательной площадки для 

студенческой молодежи, способствующей развитию студенческого самоуправления в ЮФУ 

в сфере культурно-массовой деятельности, совершенствование контактов и сотрудничества 

между студентами ЮФУ, личностное развитие и оздоровление студенческой молодежи, а 

также формирование у студентов, единых духовных и культурных ценностей в традициях 

классического образования. </p>

<p>В рамках смены предполагается проведение лекций, тренингов, практических 

занятий, репетиций с высококвалифицированными педагогами-практиками по актерскому 

мастерству – актеры театра им. Чехова г. Таганрог, сценическому движению – режиссер-

постановщик, действительный член Российской Гильдии режиссеров и педагогов по 

пластике, режиссуре – режиссеры-дипломанты и призеры российских и международных 

театральных фестивалей, дизайну костюмов – педагоги академии архитектуры и искусств 

ЮФУ, хореографии, вокалу – педагоги с многолетним стажем работы руководители 

творческих коллективов.</p>

<p>Обучение по программе производится методом погружения и предполагает 

постоянную включенность студента в образовательный процесс путем смены видов 

деятельности: мастер-классы по направлениям, работа в малых группах (тренинги на 

командообразование, тренинги личностного роста) практические занятия, репетиции, 

творческие воркшопы, культурно-массовые мероприятия. </p>

<p>Обучение методом погружения позволит охватить большой объем материала за 

короткий срок.</p>

<p>В результате обучения студенты получат практические знания по актерскому 

мастерству, сценическому движению, сценографии и созданию сценического костюма, 

режиссуре, организации и проведению культурно-массовых мероприятий. По итогам 

обучения студенты приобретут такие социально-личностные компетенции как как 

обучаемость, организованность, самостоятельность, ответственность, самоконтроль и 

самопланирование, потребность в реализации своего личностного потенциала, надежность, 

чувство долга, ориентации на ценности, терпимость, общая культура.</p>

<p>Использование коллективных форм обучения позволит увеличить количество 

социальных и межличностных связей между студентами, повысить сплоченность, 

взаимопонимание и взаимопомощь, развить навыки работы в группе, научить объяснять, 

слушать и понимать собеседника, учитывать мнение других. Стимуляция делового общения 

студентов при выполнении творческих заданий развивает коммуникативную компетенцию 

студентов и повышает их ответственность за формирование межличностных связей в 

коллективе.</p>

<p>По итогам посещения тематической смены «Творческая деятельность как 

метод формирования социально-личностных компетенций конкурентоспособного 

специалиста» станет получение каждым участником удостоверения о повышении 

квалификации установленного образца.</p>
