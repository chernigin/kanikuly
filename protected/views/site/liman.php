			<h3>О курорте</h3>
			<p>
			<big><b>«Лиманчик»</b></big> — база отдыха («спортивно-оздоровительный лагерь») 
            Южного федерального университета, основана в 1956 году, 
            находится на берегу Чёрного моря примерно в 30 км от Новороссийска, 
            между посёлками Южная Озереевка и Дюрсо. Административно относится к с.
            Абрау-Дюрсо Новороссийска. Название базе дало проточное пресное озеро,
            отделенное от моря каменным валом, используемым как пляж.
			</p>
		</div>
