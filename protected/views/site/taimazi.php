       <p>
	В сердце Кавказа, среди высоких и могучих гор Северной Осетии затаилось волшебное по красоте, пронизанное дружелюбием ущелье. Густой лес, бурные реки, серебряные ледники помогают обрести спокойствие, забыть о насущных проблемах и в добром общении найти новых друзей.
        </p>
	<p>
	Уникальный горный климат, величественные вершины, живописное ущелье, многочисленные водопады создают условия для самого разнообразного отдыха в этом районе.
	</p>
	<p>
	База практики и спортивно-оздоровительного туризма <big><b>«Таймази»</b></big> ЮФУ самая верхняя из туристических баз Дигорского ущелья, расположенная на уровне 2000 метров над уровнем моря приветствует своих гостей!
	</p>
	<p>
	Возможности окружающей природы, а также оснащенность базы позволяет получить начальные навыки подготовки туристов, альпинистов, горнолыжников а также заниматься такими современными экстремальными  видами спорта как ледолазание и парапланеризм.
	</p>
	<p>
	В окрестностях базы возможен сбор экологически чистых лекарственных трав, ягод, грибов, лесных орехов. Безопасные тропы позволяют совершать самостоятельные прогулки семьями, включая маленьких детей.
	</p>
	<p>
	В самое лучшее время летнего сезона, с 26.07.2014  по 09.08.2014 будет проводиться «Детская смена».  Приглашаем родителей с детьми возрастом от 4-х до 17 лет. Опыт прошлых лет показывает, что организованный  досуг, включающий походы, игры, физическую подготовку, занятия музыкой, рисованием и пением, общением друг с другом очень нравится детям, на это время они забывают о существовании компьютера и телевизора.
	</p>
